def translate(text: str, explicit: dict, phrases: list[tuple[str, str]]) -> str:
    if text in explicit:
        return explicit[text]
    out = text
    for src, dst in sorted(phrases, key=lambda x: -len(x[0])):
        out = out.replace(src, dst)
    return out


def build_map(src_values: set[str], explicit: dict, phrases: list[tuple[str, str]]) -> dict:
    return {v: translate(v, explicit, phrases) for v in sorted(src_values)}


